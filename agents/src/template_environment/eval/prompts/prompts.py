TOOL_USEFULNESS_PROMPT = (
    "Tool Name: {tool_name}\n\n"
    "Tool Description: {tool_description}\n\n"
    "Tool Arguments: {tool_arguments}\n\n"
    "Tool Results: {tool_results}\n\n"
    "Based on the information above, score the quality of the tool "
    "based on the following criteria:\n"
    "- expectedness: Is the tool results as expected based on "
    "given tool name and tool description?\n"
    "- fidelity: The degree of exactness of the tool result\n"
    "- usability: how usable is the result of the tool with regards to "
    "intention of the tool call inferred from the tool arguments\n"
    "- completeness: how complete the tool result is based on the "
    "tool arguments and tool description\n\n"
    "You should provide a final score on the scale of 1 - 5 based on "
    "the criteria, and the reason in a few short sentences."
)

STEPWISE_EVALUATION_PROMPT = (
    "Current trajectory: {strategy}\n\n"
    "Assistant's persona: {system_prompt}\n\n"
    "Assistant's tools: {available_tools}\n\n"
    "Current task: {user_prompt}\n\n"
    "Previous steps: \n\n"
    "{previous_responses}\n\n"
    "Current step: \n\n"
    "{current_response}\n\n"
    "Based on the current trajectory taken by an agent, its current task, "
    "previous and current steps, evaluate the current step taken by the agent "
    "based on the following criteria:\n"
    "- invoked tool correctness: is the current tool invoked a good fit "
    "for the current task\n"
    "- contextual coherence: ability to incorporate information from "
    "earlier context from previous steps\n"
    "- response completeness: whether the agent response contains "
    "complete information from its previous steps\n"
    "- tool result correctness: is the invoked tool giving the correct "
    "intended result\n"
    "- role adherence: how well is the assistant adhering to its role "
    "given by its persona\n\n"
    "Score each of the criteria on a scale of 1 - 5 (can be a float value).\n"
    "The current trajectory shows the steps taken by the agent up till "
    "the current step.\n"
    "Reason about the current step first before providing the scores.\n\n"
)
